package com.anyun100.storage.monitor.client_config_service.domain.dto;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class RegionCenterDto {

	private Long id;
	private String regionName;
	private String location_id;
	private String dbIp;
	private Long dbPort;
	private String msIp;
	private Long msPort;
	private int clientNum;
	private Long status;
	public RegionCenterDto(Long id, String regionName, String location_id, String dbIp, Long dbPort, String msIp,
			Long msPort, int clientNum, Long status) {
		this.id = id;
		this.regionName = regionName;
		this.location_id = location_id;
		this.dbIp = dbIp;
		this.dbPort = dbPort;
		this.msIp = msIp;
		this.msPort = msPort;
		this.clientNum = clientNum;
		this.status = status;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
	public String getLocation_id() {
		return location_id;
	}
	public void setLocation_id(String location_id) {
		this.location_id = location_id;
	}
	public String getDbIp() {
		return dbIp;
	}
	public void setDbIp(String dbIp) {
		this.dbIp = dbIp;
	}
	public Long getDbPort() {
		return dbPort;
	}
	public void setDbPort(Long dbPort) {
		this.dbPort = dbPort;
	}
	public String getMsIp() {
		return msIp;
	}
	public void setMsIp(String msIp) {
		this.msIp = msIp;
	}
	public Long getMsPort() {
		return msPort;
	}
	public void setMsPort(Long msPort) {
		this.msPort = msPort;
	}
	public int getClientNum() {
		return clientNum;
	}
	public void setClientNum(int clientNum) {
		this.clientNum = clientNum;
	}
	public Long getStatus() {
		return status;
	}
	public void setStatus(Long status) {
		this.status = status;
	}
	
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.SHORT_PREFIX_STYLE); 
	}
}
