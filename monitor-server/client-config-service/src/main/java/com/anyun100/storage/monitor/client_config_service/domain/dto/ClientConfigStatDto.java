package com.anyun100.storage.monitor.client_config_service.domain.dto;


import java.io.Serializable;

public class ClientConfigStatDto implements Serializable{
   
    private Long value;

    private String name;

	public ClientConfigStatDto(Long value, String name) {
		this.value = value;
		this.name = name;
	}

	public Long getValue() {
		return value;
	}

	public void setValue(Long value) {
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
